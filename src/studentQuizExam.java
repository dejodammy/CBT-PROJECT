    public void answerSelect() {
        selectedAnswer = ""; 

        if (jRadioButton1.isSelected()) {
            selectedAnswer = jRadioButton1.getText();
        } else if (jRadioButton2.isSelected()) {
            selectedAnswer = jRadioButton2.getText();
        } else if (jRadioButton3.isSelected()) {
            selectedAnswer = jRadioButton3.getText();
        } else if (jRadioButton4.isSelected()) {
            selectedAnswer = jRadioButton4.getText();
        }

        // Update the selected answers array
        if (selectedAnswers.size() >= questionId) {
            // Replace the previous answer with the new one
            selectedAnswers.set(questionId - 1, selectedAnswer);
        } else {
            // Add the new answer to the list
            selectedAnswers.add(selectedAnswer);
        }
        
        try {
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("SELECT answer FROM question WHERE id='" + questionId + "'");

            if (rs.next()) {
                String correctAnswer = rs.getString("answer");

                // Compare the selected answer with the correct answer
                if (selectedAnswer.equals(correctAnswer)) {
                    marks = marks + 1;
                }
            }
            updateMarksLabel(); // Add this line to update the marks label
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

    private void updateMarksLabel() {
        jLabel14.setText("Marks: " + marks);
    }



    public void submit() {
        int choice = JOptionPane.showConfirmDialog(
                this,
                " Do you want to submit?",
                "Submit Confirmation",
                JOptionPane.YES_NO_OPTION);

        if (choice == JOptionPane.YES_OPTION) {
            updateSelectedAnswersLabel(); // Add this line to update the selected answers label
            updateMarksLabel();
            jRadioButton1.setText("");
            jRadioButton2.setText("");
            jRadioButton3.setText("");
            jRadioButton4.setText("");
        }

    }

