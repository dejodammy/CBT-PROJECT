    private void updateSelectedAnswersLabel() {
        StringBuilder answerText = new StringBuilder("<html>Selected Answers:<br>");

        for (int i = 0; i < selectedAnswers.size(); i++) {
            answerText.append("Q").append(i + 1).append(": ").append(selectedAnswers.get(i)).append("<br>");
        }

        answerText.append("</html>");

        // Update the jTextArea1 only when the "SUBMIT" button is clicked
        jTextArea1.setText(answerText.toString());
    }

